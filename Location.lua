        function(result, headers, status_code)
            local function parse_auto_update_result(result, headers, status_code)
                local error_prefix = "Error downloading auto-updater: "
                if status_code ~= 200 then util.toast(error_prefix..status_code, TOAST_ALL) return false end
                if not result or result == "" then util.toast(error_prefix.."Leere Datei gefunden.", TOAST_ALL) return false end
                filesystem.mkdir(filesystem.scripts_dir() .. "lib")
                local file = io.open(filesystem.scripts_dir() .. "lib\\auto-updater.lua", "wb")
                if file == nil then util.toast(error_prefix.."Datei konnte nicht zum Schreiben geöffnet werden.", TOAST_ALL) return false end
                file:write(result) file:close() util.toast("Auto-Updater-Bibliothek erfolgreich installiert", TOAST_ALL) return true
            end
            auto_update_complete = parse_auto_update_result(result, headers, status_code)
        end, function() util.toast("Fehler beim Herunterladen der Auto-Updater-Lib. Update konnte nicht heruntergeladen werden.", TOAST_ALL) end)
